package be.engine.data;

public class Vector2 {

    /* FIELDS */

    private float x, y;

    /* CONSTRUCTOR */

    public Vector2(float x, float y) {
        this.x = x;
        this.y = y;
    }

    /* GETTERS & SETTERS */

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}